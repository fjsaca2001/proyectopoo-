    // busqueda de lo ingresado
    private void txtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarActionPerformed
        //variables
        String nombre_buscar = txtBusqueda.getText();
        // asignaciopn de los espacios que se presentaran si no existe el dato
        lblNombre.setText("No existe el producto");
        lblStock.setText("-----");
        lblPrecio.setText("------");
        b = false;
        for (Producto p : Registro.productos) {
            //Verificamos el nombre a buscar este contenido 
            if (p.getNombre().contains(nombre_buscar.toLowerCase())) {
                b = true;
                lblNombre.setText(p.getNombre());
                lblStock.setText(String.valueOf(p.getCantidad()));
                lblPrecio.setText(String.valueOf(p.getPrecio()));
                // guardado del nombre y precio en otro objeto para usarlo despues
                pAux.setNombre(p.getNombre());
                pAux.setPrecio(p.getPrecio());
                pAux.setCantidad(p.getCantidad());
            }
        }
    }//GEN-LAST:event_txtBuscarActionPerformed

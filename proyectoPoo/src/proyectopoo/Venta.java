    // boton que registra la cantidad
    private void btCantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCantidadActionPerformed
        // TODO add your handling code here:
        // el boton funcionara solo si existe el producto
        if (b) {
            // lectura del dato
            int cantidad = Integer.parseInt(txtcantidad.getText());
            // validacion si se puede comprar esa cantidad
            if (cantidad <= pAux.getCantidad()) {
                // almacenamos en el objeto la cantidad
                pAux.setCantidad(cantidad);
                // calculamos el subtotal
                pAux.calcularSubtotal();
                // sumamos el subtotal para la presentacion final
                subtotal += pAux.getSubtotal();
                // visualizacion previa de los datos
                txtMensaje1.setText("Nombre del producto");
                txtMensaje2.setText("Cantidad");
                txtMensaje3.setText("Precio unitario");
                txtMensaje4.setText("Precio sin iva");
                txtGetNombre.setText(pAux.getNombre());
                txtGetCantidad.setText(String.valueOf(cantidad));
                txtGetPrecioUni.setText(String.valueOf("$" + pAux.getPrecio()));
                txtGetPrecio.setText(String.valueOf("$" + pAux.getSubtotal()));

                // en caso de poderse se presentara..
                lblCantidad.setText("Agregado");
                // limpieza de las casillas
                clearList();
            } else {
                // si no existe se presentara...
                lblCantidad.setText("Verifique el Stock");
                // limpieza de la casilla de cantidad
                txtcantidad.setText("");
            }
        }
    }//GEN-LAST:event_btCantidadActionPerformed
// generar los valores finales
    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // almacenamiento del subtotal
        pAux.setSubtotal(subtotal);
        // calculo del iva
        pAux.calcularIva();
        // calculo del total
        pAux.calcularTotal();
        // presentacion de los valores obtenidos
        txtGetSubtotal.setText("Subtotal: $" + pAux.getSubtotal());
        txtGetIva.setText("Iva: $" + pAux.getIva());
        txtGetTotal.setText("Total: $" + pAux.getTotal());
    }//GEN-LAST:event_btnGenerarActionPerformed

